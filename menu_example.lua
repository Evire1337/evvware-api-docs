--[[
Что бы скрыть меню виджет вы должны указать перед его созданием какую то переменную по типу 'local checkbox1 = ...'
теперь что бы нам скрыть этот элемент (что бы он не рисовался) нужно написать следующие:

checkbox1:visible(false)

^^^ Это значит что мы 'выключаем' отрисовку этого меню виджета, так же если будет значение true то это включит отрисовку обратно

Прошу заметить что ':visible(bool visible)' не работает на элементы которые создаются в 'settings'
]]
checkbox1:visible(false)

--[[
что бы получить значение самих меню виджетов нельзя напрямую это сделать по типу 'print(checkbox1)'
для каждого типа значения существует каждая функция, для примера такие как:

:get_bool() -> возвращает значение меню виджета типа bool (true/false)

:get_int() -> возвращает значение меню виджета типа int, то есть целых чисел

:get_float() -> возвращает значение меню виджета типа float, то есть целых чисел после запятой

:get_color() -> возвращает значение меню виджета типа 'color', в луа документации вы можете найти что в себе имеет этот класс

:get_keybind() -> возвращает значение меню виджета типа 'c_key_bind', в луа документации вы можете найти что в себе имеет этот класс

:get_table() -> возвращает значение меню виджета типа luatable, то есть обычной луа таблицы

]]
client.print(tostring(checkbox1:get_bool()))

--[[
Так же можно изменить значение для меню виджета тому элементу, которому это было прописано, для примера:

checkbox1:set_bool(true)

^^^
Функция установит значение чекбоксу на true
Для каждого типа значения существует каждая функция, для примера такие как:

:set_bool(bool new_bool) -> уставливает значение меню виджета типа bool

:set_int(int new_int) -> уставливает значение меню виджета типа int, то есть целых чисел

:set_float(float new_float) -> уставливает значение меню виджета типа float, то есть целых чисел после запятой

:set_color(color new_color) -> уставливает значение меню виджета типа 'color', в луа документации вы можете найти что в себе имеет этот класс

:set_keybind(c_key_bind new_keybind) -> уставливает значение меню виджета типа 'c_key_bind', в луа документации вы можете найти что в себе имеет этот класс

]]
checkbox1:set_bool(true)


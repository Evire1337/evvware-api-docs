--[[
Создаёт фрейм (кадр, вместилище) под все ваши виджеты. Этот фрейм привязывается к вашему 'ui_manager' в последовательности в которой
вы вызвали функцию 'add_frame', то есть все фреймы будут отрисовываться в последовательности их создания

'add_frame' возвращает 'ui_widget_frame' класс который имеет в себе не мало важные 2 функции такие как
frame:hide() -> отключает ваш фрейм, после вызова каждый элемент в вашем фрейме не будет рисоваться
frame:show() -> включает ваш фрейм, после вызова каждый элемент в вашем фрейме будет рисоваться нормально

при создании фрейма он всегда включён
]]
local new_frame = ui_manager:add_frame()
local frame = ui_manager:add_frame()


